public class Numeric {
    public static boolean isOdd(int x) {
        return x % 2 == 1;
    }

    public static boolean isEven(int x) {
        return x % 2 == 0;
    }

    public static int square(int x) {
        return x * x;
    }

    public static int cube(int x) {
        return x * x * x;
    }

}